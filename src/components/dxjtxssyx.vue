            <span style="font-weight: bold;font-size: 19px;font-family: 仿宋_GB2312">&nbsp;</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-weight: bold;font-size: 19px;font-family: 仿宋_GB2312">第九条&nbsp;</span><span style="font-size: 19px;font-family: 仿宋_GB2312">&nbsp;对基金管理人的审慎调查得分，按各单项得分加总计算，公司代销基金的管理人审慎调查分值应当在60分以上。</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-weight: bold;font-size: 19px;font-family: 仿宋_GB2312">第十条&nbsp;&nbsp;</span><span style="font-size: 19px;font-family: 仿宋_GB2312">对基金管理人进行审慎调查的方法：</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">（一）对基金公司的公开披露的信息进行分析，包括对基金公司的规模、注册资金、注册地址、法定代表人、公司高管、投研团队、公司公告等信息披露进行分析评价；</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">（二）对相关管理机构发布和披露的公开信息进行分析，重点分析基金公司的诚信状况和内部控制情况进行分析；</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">（三）对基金公司的投资业绩进行分析，包括对基金的历史业绩和收益波动进行分析；</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">（四）对基金公司提供的其他材料进行分析，在基金公司提供的材料中如果包含非公开信息的，公司基金业务部必须对信息的适当性实施尽职甄别。</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">&nbsp;</span>
        </p>
        <p style="text-indent: 134px;margin-bottom: 0;margin-top: 10px;text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-weight: bold;font-size: 21px;font-family: 黑体">第三章&nbsp;&nbsp;基金产品风险评价</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-weight: bold;font-size: 19px;font-family: 仿宋_GB2312">第十一条</span><span style="font-size: 19px;font-family: 仿宋_GB2312">&nbsp;&nbsp;对基金产品的风险评价，由公司研究发展部负责。基金产品风险评价结果作为公司向基金投资人推介基金产品的重要依据。</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-weight: bold;font-size: 19px;font-family: 仿宋_GB2312">第十二条</span><span style="font-size: 19px;font-family: 仿宋_GB2312">&nbsp;&nbsp;公司研究发展部根据《证券投资基金法》</span><span style="font-size: 19px;font-family: 仿宋_GB2312">、</span><span style="font-size: 19px;font-family: 仿宋_GB2312">《证券投资基金运作管理办法》</span><span style="font-size: 19px;font-family: 仿宋_GB2312">、</span><span style="font-size: 19px;font-family: 仿宋_GB2312">《证券投资基金销售适用性指导意见》等法律</span><span style="font-size: 19px;font-family: 仿宋_GB2312">、规章</span><span style="font-size: 19px;font-family: 仿宋_GB2312">和规范性文件确定基金产品风险评价</span><span style="font-size: 19px;font-family: 仿宋_GB2312">方法</span><span style="font-size: 19px;font-family: 仿宋_GB2312">。</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-weight: bold;font-size: 19px;font-family: 仿宋_GB2312">第十三条</span><span style="font-size: 19px;font-family: 仿宋_GB2312">&nbsp;&nbsp;基金产品风险评价以基金产品的风险等级来具体反映。基金产品共设五个风险等级，按风险由低到高依次是：“低风险级”</span><span style="font-size: 19px;font-family: 仿宋_GB2312">、</span><span style="font-size: 19px;font-family: 仿宋_GB2312">“较低风险级”</span><span style="font-size: 19px;font-family: 仿宋_GB2312">、</span><span style="font-size: 19px;font-family: 仿宋_GB2312">“中风险级</span><span style="font-size: 19px;font-family: 仿宋_GB2312">”</span><span style="font-size: 19px;font-family: 仿宋_GB2312">、</span><span style="font-size: 19px;font-family: 仿宋_GB2312">“较高风险级”和“高风险级”。</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-weight: bold;font-size: 19px;font-family: 仿宋_GB2312">第十四条&nbsp;</span><span style="font-size: 19px;font-family: 仿宋_GB2312">&nbsp;基金产品风险等级划分依据以下方面进行:</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">（一）基金招募说明书所明示的投资方向、投资范围和投资比例；</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">（二）基金实际资产配置、持仓情况；</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">（三）基金的过往业绩及基金净值历史波动程度；</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">（四）基金的历史规模和持仓比例，以及股票换手率情况；</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">（五）基金的持股集中度；</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">（六）基金有无管理不当、操作失误、违规行为发生；</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">（七）其他相关因素。</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-weight: bold;font-size: 19px;font-family: 仿宋_GB2312">第十五条</span><span style="font-size: 19px;font-family: 仿宋_GB2312">&nbsp;&nbsp;基金产品风险等级的评定采取“逐步归类”的方法进行。</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">（一）第一步：依据基金招募说明书所明示的投资方向、投资范围、投资比例以及基金最近一年内有无违规行为发生初步划定基金风险等级范围；</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">1、低风险级：保本基金、&nbsp;货币市场基金；</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">2、较低风险级：货币市场基金、债券型基金；</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">3、中风险级：债券型基金、保守配置型基金、积极配置基金；</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">4、较高风险级：积极配置型基金、股票型基金；</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">5、高风险级：最近一年内有违规行为发生的基金。</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">（二）第二步：在第一步划分的基础上，根据基金最近连续三个季报公布的实际投资组合以及持仓比例对基金初步分类进行调整；对于成立期限不足一年的基金暂按其招募说明书中所述划定。</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">（三）第三步：根据所有基金最近一年净值波动率情况对基金进行排序，计算在前两步的基础上划分的各风险等级基金产品一年期净值平均波动率水平。若某只基金波动率水平高于其更高风险等级基金产品的平均波动率水平，则调高其风险等级至更高等级；反之若其波动率水平低于更低风险等级基金产品的平均波动率水平，则调低其风险等级至更低等级。对于最近一年净值波动率排名所有基金前3%的基金，直接列入“高风险级”并提示原因。</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">（四）第四步：根据在前三步的基础上划分的各风险等级基金产品的最近一年净值增长情况对各风险等级内的所有基金进行排序，若某只基金的净值增长排名在相应风险等级所有基金产品的后20%，则调高其风险等级一级。对于最近一年净值增长在相应风险等级所有基金产品的后10%的基金，直接列入“高风险级”并提示原因。</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">（五）第五步：经过前四步的逐级归类后，对于风险等级处于中风险级、较高风险级的基金，为了更好的衡量其风险水平，根据其历史规模大小、换手率、持股集中度情况进一步进行风险等级调整。若基金当期规模变动超过&nbsp;200%，则调高其风险等级一级；对于规模超过当期全市场流通市值千分之四且当期规模变动超过&nbsp;200%的基金，直接列入“高风险级”并提示原因；对于换手率超过&nbsp;1000%且换手率排名在所有基金中排名前10%的基金，直接列入“高风险级”并提示原因；对于持股集中度排名在所有基金中排名前&nbsp;3%的基金，直接列入“高风险级”并提示原因。</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">（六）第六步：在前五步划分的基础上，对于最近一年基金有重大管理不当，严重操作失误的基金，在其原风险等级基础上调高一级。</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">（七）经过前六步的“逐步归类”，最终确定基金产品的风险等级。</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-weight: bold;font-size: 19px;font-family: 仿宋_GB2312">第十六条&nbsp;</span><span style="font-size: 19px;font-family: 仿宋_GB2312">&nbsp;基金产品风险评价频度为：新发基金每只评价一次，已发行基金产品每季度更新一次评价结果。由研究发展部出具基金产品的风险评价结果，并由基金业务部进行系统设置。</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">&nbsp;</span>
        </p>
        <p style="margin: 0 0 0 66px;text-align: center;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-weight: bold;font-size: 21px;font-family: 黑体">第四章&nbsp;基金投资人风险承受能力调查和评价</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-weight: bold;font-size: 19px;font-family: 仿宋_GB2312">第十七条&nbsp;</span><span style="font-size: 19px;font-family: 仿宋_GB2312">&nbsp;投资人风险承受能力的调查和评价由公司业务管理部和基金业务部联合负责。</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-weight: bold;font-size: 19px;font-family: 仿宋_GB2312">第十八条&nbsp;&nbsp;</span><span style="font-size: 19px;font-family: 仿宋_GB2312">调查方式</span>
        </p>
        <p style=";text-align: justify;font-size: 14px;font-family: &#39;Times New Roman&#39;;white-space: normal;line-height: 21px">
            <span style="font-size: 19px;font-family: 仿宋_GB2312">（一）客户通过中信建投期货网站基金网上交易系统自助填写调查表；</span>
        </p>
